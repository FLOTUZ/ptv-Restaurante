    private void btn_irActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_irActionPerformed

        NotaVentaDAO ndao = new NotaVentaDAO(new Conector.Conector().conectarMySQL());

        int folio = Integer.parseInt(tf_folio.getText());
        Nota_VentaVO nota = ndao.getNota_ByID(folio);

        lbl_fecha.setText(nota.getFecha());
        lbl_hora.setText(nota.getHora());
        lbl_mesa.setText(String.valueOf(nota.getMesa()));
        
        lbl_mesero.setText(String.valueOf(nota.getMesero_idmesero()));

        ArrayList<Nota_Detalles> detalle = ndao.consultaDetallesDeNota(folio);

        int r = 0;
        double total = 0;
        for (Nota_Detalles d : detalle) {
            tb_detalle.setValueAt(d.getSku(), r, 0);
            tb_detalle.setValueAt(d.getNombre(), r, 1);
            tb_detalle.setValueAt(d.getCantidad(), r, 2);
            tb_detalle.setValueAt(d.getPrecioUnitario(), r, 3);
            total += d.getPrecioUnitario();

            r++;
        }

        lbl_total.setText(String.valueOf(total));
    }//GEN-LAST:event_btn_irActionPerformed

